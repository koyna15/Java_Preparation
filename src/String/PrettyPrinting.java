package String;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 2323.2345f;
        System.out.printf("Formatted num is %.2f", a);
        System.out.println();
        System.out.printf("Pi: %.3f", Math.PI);
        System.out.println();
        System.out.printf("hello my name is %s and I am %s", "chicki","Cool");
    }
}
